    <h1>Privacy Policy</h1>
    <section>
        <h2>Introduction</h2>
        <p>
            In compliance with the UP Diliman Privacy Policy, complemented by the UP Diliman Data Subject Rights and
            Responsibilities, and relevant data privacy regulations, DRAP: Draft Ranking Automated Processor respects
            your privacy and safeguards your personal data. This Privacy Policy notice serves to inform the users of
            DRAP the purpose of the collection of student data and the extent of the processing of that data. By using
            this web application, you consent to the practices described in this policy.
        </p>
    </section>
    <section>
        <h2>Information We Collect</h2>
        <p>We collect personal information that you willingly provide when utilizing this application.</p>
        <ul>
            <li><strong>Full name:</strong> for the personal identification of the student.</li>
            <li><strong>Student number:</strong> for ensuring the validity of your student information.</li>
            <li><strong>UP email address:</strong> for communication purposes.</li>
            <li>
                <strong>Google user ID:</strong> for authentication and authorization through Google OpenID Connect via OAuth
                2.0.
            </li>
            <li>
                <strong>Other data:</strong> includes student lab preferences and rankings by students and faculty members.
            </li>
        </ul>
        <p>This information is collected during user registration and draft process.</p>
    </section>
    <section>
        <h2>How We Use Your Information</h2>
        <p>We utilize your data for automating the process of the yearly draft of research lab assignments.</p>
    </section>
    <section>
        <h2>Data Security Measures</h2>
        <p>
            Security measures are implemented to protect your personal information from unauthorized access, and
            disclosure. This includes measures inherent to Google OAuth 2.0 and those of the cloud hosting provider <a
                target="_blank"
                href="https://fly.io/">Fly.io</a
            >.
        </p>
    </section>
    <section>
        <h2>Sharing of Information</h2>
        <p>
            We do not sell nor trade your personal information to third parties. However, your data is shared with
            trusted service providers necessary for the operation and provision of services of DRAP (e.g., Google OAuth
            2.0 and Fly.io).
        </p>
    </section>
    <section>
        <h2>Your Rights</h2>
        <ul>
            <li>
                <strong>Access.</strong> You have the right to access the personal information we hold about you. Feel free
                to contact us for a copy of your data.
            </li>
            <li>
                <strong>Correction.</strong> If you believe the personal information we hold about you is inaccurate or incomplete,
                you may request corrections.
            </li>
            <li><strong>Deletion.</strong> You may request the deletion of your personal information.</li>
        </ul>
    </section>
